    //button click information
    public void flip(View v)
    {
        TextView t1 = (TextView)findViewById(R.id.CoinFlipOutput);

        //create rand variable everytime button is clicked
        Random rand = new Random();
        int num = rand.nextInt(2)+1;

        //if statement to return the random number as a string
        if(num <= 1)
        {
            t1.setText("Heads");
        }
        else
            t1.setText("Tails");

    }


